const LanguageContext = React.createContext<{
  locale: LocaleType;
  setLocale: (lang: LocaleType) => void;
  t: (key: keyof typeof translations['en']) => string;
}>({
  locale: 'en',
  setLocale: () => {},
  t: (key) => key,
});

export const LanguageProvider: React.FC<{ children: React.ReactNode }> = ({ children }) => {
  const [locale, setLocaleState] = useState<LocaleType>('en');

  useEffect(() => {
    // Load stored language preference on mount
    AsyncStorage.getItem(LOCALE_KEY).then((storedLocale) => {
      if (storedLocale && (storedLocale === 'en' || storedLocale === 'hi' || storedLocale === 'bn' || storedLocale === 'bho')) {
        setLocaleState(storedLocale as LocaleType);
      }
    });
  }, []);

  const setLocale = async (newLocale: LocaleType) => {
    setLocaleState(newLocale);
    await AsyncStorage.setItem(LOCALE_KEY, newLocale);
  };

  const t = (key: keyof typeof translations['en']): string => {
    return translations[locale][key] || translations['en'][key] || String(key);
  };

  return (
    <LanguageContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </LanguageContext.Provider>
  );
};

export const useLanguage = () => React.useContext(LanguageContext);
